#!/usr/bin/env python3
"""Accuracy benchmark runner — tests FindEvil against ground-truth image.

Usage:
    python scripts/run_accuracy_benchmark.py

Generates BENCHMARK_RESULTS.md with pass rates, precision/recall, and
any discrepancies between agent output and known ground truth.
"""

from __future__ import annotations

import json
import subprocess
import sys
from datetime import datetime, timezone
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
TEST_FILE = REPO_ROOT / "tests" / "test_ground_truth.py"
OUTPUT_FILE = REPO_ROOT / "BENCHMARK_RESULTS.md"
GROUND_TRUTH_IMAGE = Path("/evidence/cases/ground_truth.raw")
GROUND_TRUTH_MANIFEST = Path("/evidence/cases/ground_truth_manifest.json")


def check_prerequisites() -> bool:
    """Verify the ground truth image exists."""
    if not GROUND_TRUTH_IMAGE.exists() or not GROUND_TRUTH_MANIFEST.exists():
        print("❌ Ground truth image not found.")
        print()
        print("Generate it with:")
        print("  sudo bash scripts/generate_ground_truth_image.sh")
        print()
        return False
    return True


def run_tests() -> tuple[int, int, str]:
    """Run the ground truth tests and return (pass, total, output_text)."""
    result = subprocess.run(
        [sys.executable, "-m", "pytest", str(TEST_FILE), "-v", "--tb=short"],
        capture_output=True,
        text=True,
        cwd=str(REPO_ROOT),
        timeout=120,
    )
    output = result.stdout + "\n" + result.stderr

    # Parse pass/total from summary line like "== 12 passed, 3 deselected in 2.00s =="
    passed = 0
    total = 0
    for line in output.splitlines():
        if "passed" in line and "failed" not in line:
            import re

            m = re.search(r"(\d+) passed", line)
            if m:
                passed = int(m.group(1))
            m = re.search(r"(\d+) failed", line)
            if m:
                passed += 0  # don't count failures as passes
            m = re.search(r"(\d+) deselected", line)
            if m:
                pass  # ignore deselected
        if "==" in line and "passed" in line:
            m = re.search(r"(\d+) passed", line)
            if m:
                passed = int(m.group(1))
            # Count total by collecting unique test names
        if line.startswith("tests/test_ground_truth.py::"):
            total += 1

    # Fallback: count test functions
    if total == 0:
        total = output.count("PASSED") + output.count("FAILED") + output.count("ERROR")

    return passed, total, output


def load_manifest() -> dict:
    """Load the ground truth manifest."""
    try:
        return json.loads(GROUND_TRUTH_MANIFEST.read_text())
    except (json.JSONDecodeError, OSError):
        return {}


def generate_report(passed: int, total: int, output_text: str) -> str:
    """Generate BENCHMARK_RESULTS.md."""
    pass_rate = (passed / total * 100) if total > 0 else 0.0
    manifest = load_manifest()
    findings = manifest.get("expected_findings", {})
    known = findings.get("known_files", [])
    deleted = findings.get("deleted_files", [])
    suid = findings.get("suid_files", [])

    lines = []
    lines.append("# Accuracy Benchmark Results")
    lines.append("")
    lines.append(f"**Run at:** {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M:%S UTC')}")
    lines.append("")
    lines.append("## Summary")
    lines.append("")
    lines.append("| Metric | Value |")
    lines.append("|---|---|")
    lines.append(f"| Tests passed | {passed}/{total} |")
    lines.append(f"| Pass rate | {pass_rate:.1f}% |")
    lines.append("")
    lines.append("## Ground Truth Artifacts")
    lines.append("")
    lines.append("| Category | Count | Expected Detections |")
    lines.append("|---|---|---|")
    lines.append(f"| Known files | {len(known)} | Marker text, YARA signatures, hash |")
    lines.append(f"| Deleted files | {len(deleted)} | File carving recovery |")
    lines.append(f"| SUID binaries | {len(suid)} | Permission flags |")
    lines.append("| Control (no FP) | 1 | Must NOT trigger malware detection |")
    lines.append("")

    # Check for failures in output
    failures = [ln for ln in output_text.splitlines() if "FAILED" in ln]
    if failures:
        lines.append("## Failures")
        lines.append("")
        for f in failures[:10]:
            lines.append(f"- {f.strip()}")
        lines.append("")

    lines.append("## Test Output")
    lines.append("")
    lines.append("```")
    # Last 30 lines of output
    out_lines = output_text.strip().splitlines()
    for ln in out_lines[-30:]:
        lines.append(ln)
    lines.append("```")
    lines.append("")
    lines.append("---")
    lines.append("*Generated by `scripts/run_accuracy_benchmark.py`*")

    return "\n".join(lines)


def main():
    if not check_prerequisites():
        sys.exit(1)

    print("Running accuracy benchmark...")
    passed, total, output_text = run_tests()
    print(output_text[:1000])

    report = generate_report(passed, total, output_text)
    OUTPUT_FILE.write_text(report)
    print(f"\n✅ Report written to {OUTPUT_FILE}")

    if total > 0:
        print(f"   Pass rate: {passed}/{total} ({passed/total*100:.1f}%)")

    if passed < total:
        print("❌ Some tests failed — check report for details.")
        sys.exit(1)
    else:
        print("🎉 All benchmarks passed!")
        sys.exit(0)


if __name__ == "__main__":
    main()
